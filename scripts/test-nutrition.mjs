function computeSignals(byDay) {
  const total = byDay.length || 1;
  const cover = {}; KEY_NUTRIENTS.forEach(n => cover[n] = 0);
  byDay.forEach(day => {
    const c = new Set();
    day.forEach(ing => (NUTRI_MAP[ing] || []).forEach(n => c.add(n)));
    c.forEach(n => { if (n in cover) cover[n]++; });
  });
  return KEY_NUTRIENTS.map(n => {
    const d = cover[n]; const ratio = d / total;
    const level = (d >= 3 || ratio >= 0.5) ? 'green' : d > 0 ? 'yellow' : 'red';
    return { nutrient: n, days: d, level };
  });
}

