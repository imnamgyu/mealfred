/**
 * lib/season.ts — 월별 제철 식재료(도감 표준명) + 박스 배송 적합성.
 * foods/season SSG 페이지와 박스 추천(lib/box.ts)이 공유한다.
 * 제철 = 영양가 높고·신선·제값. 박스는 제철 우선으로 '제철 영양' 강점을 만든다.
 */
export const SEASON_MAP: Record<number, string[]> = {
  1: ['배추', '무', '시금치', '대구', '명태'],
  2: ['시금치', '달래', '대구', '명태'],
  3: ['딸기', '달래', '쑥', '냉이', '미나리'],
  4: ['딸기', '달래', '쑥', '쪽파'],
  5: ['딸기', '마늘', '참외'],
  6: ['감자', '마늘', '갈치'],
  7: ['수박', '복숭아', '옥수수', '오이', '가지'],
  8: ['수박', '복숭아', '옥수수', '가지', '전복', '오징어'],
  9: ['배', '포도', '감', '연근', '고등어', '전어'],
  10: ['배', '감', '사과', '밤', '고구마', '연어'],
  11: ['배', '사과', '감', '무', '배추', '대구'],
  12: ['귤', '배추', '무', '시금치', '대구', '명태'],
};

/** 그 달 제철인가 (1~12월). */
export function inSeason(nm: string, month: number): boolean {
  return (SEASON_MAP[month] || []).includes(nm);
}

/** 박스 배송 시 산폐·신선도 위험으로 제외할 '신선 해산물'.
 *  생선·갑각/조개는 운송 중 산폐·상함 위험 → 박스 제외. 단 건어물(멸치·가다랑어)·해조류(별도 cat)는 안전해 유지. */
const SHELF_STABLE_SEA = new Set(['멸치', '가다랑어']);
export function isPerishableSeafood(nm: string, cat: string): boolean {
  return (cat === '생선' || cat === '갑각_조개') && !SHELF_STABLE_SEA.has(nm);
}
