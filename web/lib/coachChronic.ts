/** 부모 입력 free-text(예: "변비, 아토피")에서 매칭되는 가이드들. 공백·대소문자 무시. */
export function matchChronic(text: string | null | undefined): ChronicGuide[] {
  if (!text) return [];
  const t = text.replace(/\s/g, '').toLowerCase();
  if (!t) return [];
  return CHRONIC_GUIDES.filter((g) => g.keywords.some((k) => t.includes(k.replace(/\s/g, '').toLowerCase())));
}

/** coach.ts에 넣을 만성질환 식이 방향 텍스트(없으면 ''). 변비+장트러블 동시면 섬유 충돌 가드. */
export function chronicGuidanceText(text: string | null | undefined): string {
  const m = matchChronic(text);
  if (!m.length) return '';
  const conds = m.map((g) => g.condition);
  let out = m.map((g) => `· ${g.condition}: ${g.framing}`).join('\n');
  if (conds.includes('변비') && conds.includes('장트러블(잦은 설사·복통)')) {
    out += `\n· (변비와 장트러블이 함께 있어요 — 무리한 섬유 제한은 피하고 속이 편한 음식 위주로 부드럽게. 증상이 오락가락하거나 심하면 소아청소년과 확인을 권해요.)`;
  }
  return out;
}
