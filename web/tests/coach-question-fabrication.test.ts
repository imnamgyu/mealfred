/**
 * 질문 환각 가드 회귀 — 아린 06-21 "어제 저녁에 배탈이 나서 조금만 드셨다고 하셨는데…카레…" 사건 박제.
 * 적대 워크플로우(84케이스, question-hallucination-adversary)로 4갈래(건강·발언전가·감정/사회/행동·미등록음식) 보강.
 * 최종: 환각 61개 중 60 차단·정상 23개 오탐 0. 새 환각 유형이 회귀하지 않게 대표 케이스를 고정한다.
 */
import { describe, it, expect } from 'vitest';
import { questionFabricates } from '../lib/coach';

// 아린형 실제 로그: 이 음식들만 '존재'. 그 밖은 전부 날조.
const allowed = new Set(['계란볶음밥', '짜파게티', '소고기무국', '밥', '두부', '감자볶음', '소시지', '간장참기름밥', '김', '메추리알장조림', '새우튀김', '돈까스']);

describe('① 건강·증상 날조 차단(health)', () => {
  it.each([
    '어제 저녁에 배탈이 나서 짜파게티를 조금밖에 못 드셨는데, 오늘은 좀 어떤가요?',
    '감기 기운 때문에 입맛이 없어서 소고기무국만 겨우 먹던데 컨디션은요?',
    '콧물이 계속 흘러서 밥을 적게 먹었던데 오늘은 멎었나요?',
    '몸살로 기운이 없어 보이던데 돈까스는 몇 입 먹었나요?',
    '장염 기운이 있어 보여서 김이랑 밥만 살살 줬는데 회복됐나요?',
    '목이 부어서 부드러운 감자볶음만 겨우 넘기던데 가라앉았나요?',
    '배가 아파서 계란볶음밥을 반도 못 비우던데 지금은 괜찮나요?',
    '기침이 심해서 새우튀김을 삼키기 힘들어하던데 어때요?',
  ])('차단: %s', (q) => { expect(questionFabricates(q, allowed)).toBe('health'); });
});

describe('② 부모 발언 전가 차단(attrib)', () => {
  it.each([
    '돈까스가 느끼했는지 반만 먹었다고 하셨죠? 소스를 줄여볼까요?',
    '김만 있으면 밥 한 그릇을 뚝딱 비운다고 하셨으니, 김을 활용해볼까요?',
    '간장참기름밥을 더 잘 먹는다고 하셨던 것 같은데 맞을까요?',
    '물을 많이 찾았다고 하셔서 걱정되셨을 텐데 지금은요?',
    '"미역국은 잘 먹는다"고 적어주셨던데 소고기를 더 넣어볼까요?',
    '"콩나물국은 잘 마셔요"라고 메모해주셔서 건더기도 권해볼까요?',
    '소시지만 찾고 다른 반찬은 거부했다고 하셨죠?',
    '계란볶음밥을 제일 좋아한다고 하셨으니 채소를 섞어볼까요?',
  ])('차단: %s', (q) => { expect(questionFabricates(q, allowed)).toBe('attrib'); });
});

describe('③ 감정·사회·행동 정황 날조 차단(emotion)', () => {
  it.each([
    '어제 속상한 일이 있어서 그런지 계란볶음밥을 적게 먹던데 기분은 풀렸나요?',
    '어린이집에서 친구가 김을 안 나눠줘서 시무룩했던 날 집에선 잘 먹었나요?',
    '동생이 새우튀김을 뺏어 먹어서 토라졌을 텐데 다시 찾던가요?',
    '선생님께 꾸중 들은 날이라 시무룩했는지 소고기무국을 남겼는데 기분은요?',
    '엄마랑 떨어지기 싫어 울던 아침 간장참기름밥은 몇 술 떴나요?',
    '동생한테 질투가 나서 떼쓰던 저녁 감자볶음은 결국 먹였나요?',
    '낯선 사람이 와서 긴장했던지 메추리알장조림에 손도 안 대던데요?',
    '소고기무국 국물만 두 대접 들이켜고 건더기는 안 건졌다던데요?',
    '소시지를 다섯 개나 연달아 먹어 치웠을 만큼 좋아하던데요?',
    '기분이 안 좋아 보이던 날 짜파게티로 달래주셨던데 나아졌나요?',
  ])('차단: %s', (q) => { expect(questionFabricates(q, allowed)).toBe('emotion'); });
});

describe('④ 미등록 음식 날조 차단(food)', () => {
  it.each([
    ['어제 카레를 그렇게 잘 먹던데 비슷한 노란 음식을 곁들여볼까요?', '카레'],
    ['김치찌개 국물을 떠먹기 시작한 큰 진전인데 건더기도 권해볼까요?', '김치찌개'],
    ['떡국을 한 그릇 비운 날 그 위에 달걀지단을 얹어볼까요?', '떡국'],
    ['고등어 살을 발라줬더니 잘 받아먹던데 다른 생선도 도전해볼까요?', '고등어'],
    ['불고기를 한 그릇 비운 날 그 고기반찬을 또 준비해볼까요?', '불고기'],
  ])('차단: %s', (q, food) => { expect(questionFabricates(q as string, allowed)).toBe('food:' + food); });
});

describe('allowedHasWord 1글자 오매칭 회귀(김 ↔ 김치찌개)', () => {
  it('allowed에 "김"이 있어도 "김치찌개"는 미등록으로 잡는다', () => {
    expect(questionFabricates('김치찌개를 잘 먹나요?', new Set(['김', '밥']))).toBe('food:김치찌개');
  });
  it('"시금치나물"은 allowed에 "시금치"(2글자+)가 있으면 통과', () => {
    expect(questionFabricates('시금치나물 잘 먹나요?', new Set(['시금치', '밥']))).toBeNull();
  });
  it('"감자"는 allowed에 "감자볶음"이 있으면 통과(로그된 재료)', () => {
    expect(questionFabricates('감자볶음의 감자, 안 남겼나요?', new Set(['감자볶음', '밥']))).toBeNull();
  });
});

describe('정상 질문은 통과(오탐 0) — 반응/혼합/환경/자율성/본보기', () => {
  it.each([
    "어제 점심 '계란볶음밥' 먹을 때 아이 표정은 어땠어요?",
    '소고기무국을 다른 반찬과 섞어 줘 보셨나요?',
    '오늘 식탁에서 아이가 스스로 골라 먹으려 했나요?',
    '짜파게티 먹을 때 영상을 보고 있었나요?',
    '두부를 식탁에 올렸을 때 아이가 만지거나 냄새 맡아봤나요?',
    '아이가 음식을 남기거나 거부할 때 어떠셨어요?',
    '부모님이 새우튀김을 같이 맛있게 드시는 모습을 보여주셨나요?',
    // FP 회귀: 정상 반응질문의 "밀어냈는지"가 행동날조로 오탐되면 안 됨
    '두부를 드렸을 때 한 입 먹어보고 더 찾았는지, 아니면 밀어냈는지 기억나시나요?',
    '감자볶음을 평소보다 잘 먹던가요, 아니면 남겼나요?',
    '어제 차려주신 돈까스, 아이가 어떻게 먹었어요?',
  ])('통과: %s', (q) => { expect(questionFabricates(q, allowed)).toBeNull(); });
});
